package Java_Training;

public class Day8_Loops {

	public static void main(String[] args) {

		
//		Syntax:
		
		
//		while(condition) {
//			
//		}
		
		
		int i = 0; 
		
		while(i > 5) {
			
			System.out.println(i);
			
			i++;
		}
		
		
		do {
			
			System.out.println(i);
			i++;
			
		}while(i < 5);
		
	}

}
